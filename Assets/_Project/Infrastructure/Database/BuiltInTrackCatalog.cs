        // ---- Билдер и помощники (StartTime/Duration из долей; рука/палец/плотность — локально) ----

        private static BuiltInTrack Track(string title, string composer, double bpm, string tonality,
            double difficulty, params (int midi, double beat, double durBeats)[] notes)
        {
            double secPerBeat = 60.0 / bpm;
            var list = new List<Note>(notes.Length);
            double endSec = 0.0;

            for (int i = 0; i < notes.Length; i++)
            {
                (int midi, double beat, double durBeats) = notes[i];
                double start = beat * secPerBeat;
                double dur = durBeats * secPerBeat;
                if (start + dur > endSec) endSec = start + dur;

                list.Add(new Note
                {
                    NoteIndex = i,
                    MidiNumber = midi,
                    StartTime = start,
                    Duration = dur,
                    Hand = HandFor(midi),
                    Finger = FingerFor(midi),
                });
            }

            double density = endSec > 0.0 ? Math.Round(notes.Length / endSec, 2) : notes.Length;

            return new BuiltInTrack
            {
                Track = new MidiTrack
                {
                    Filename = null, // встроенный трек без исходного файла (UNIQUE допускает несколько NULL)
                    Title = title,
                    Composer = composer,
                    Bpm = bpm,
                    Tonality = tonality,
                    Difficulty = difficulty,
                    NoteDensity = density,
                },
                Notes = list,
            };
        }

        /// <summary>Граница рук как в <c>AutoFingering</c>: ноты ниже C4 (MIDI 60) — левая рука.</summary>
        private static string HandFor(int midi) => midi < 60 ? "left" : "right";

        //                                       C  C# D  D# E  F  F# G  G# A  A# B
        private static readonly int[] FingerByPc = { 1, 2, 2, 3, 3, 1, 2, 2, 3, 3, 4, 4 };

        /// <summary>Палец 1..5 по классу высоты (детерминированно, всегда проходит CHECK finger BETWEEN 1 AND 5).</summary>
        private static int FingerFor(int midi) => FingerByPc[((midi % 12) + 12) % 12];
